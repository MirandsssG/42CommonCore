#include <unistd.h>

int main (int ac, char **av)
{
    int i = 0;
    if (ac == 2)
    {
        while (av[1][i] != '\0')
        {
            char c = av[1][i];
            if (av[1][i] >= 'a' && av[1][i] <= 'z')
                c = c - 32;
            else if (av[1][i] >= 'A' && av[1][i] <= 'Z')
                c = c + 32;
            write (1, &c, 1);
            i++;
        }
    }
    write (1, "\n", 1);
}