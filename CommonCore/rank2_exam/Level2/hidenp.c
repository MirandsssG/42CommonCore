#include <unistd.h>

int main(int ac, char **av)
{
    int i = 0;
    int j = 0;

    if (ac == 3)
    {
        while (av[2][i])
        {
            while (av[1][j] == av[2][i])
                j++;
            if (!av[1][j])
            {
                write(1, &"1\n", 2);
                return (0);
            }
            i++;
        }
        write (1, &"0", 1);
    }
    write (1, &"\n", 1);
    return (0);
}