#include <unistd.h>

void	ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = 0;
	while (s[i])
	{
		write (fd, &s[i], 1);
		i++;
	}
}

int	main(void)
{
	ft_putstr_fd("Teste 123\n", 1);

	ft_putstr_fd("Erro\n", 2);

	return (0);
}
